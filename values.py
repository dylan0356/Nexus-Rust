def calculate(gunDelta: list) -> list:
    
    NewGunList = []
    
    last_shot_x = 0
    last_shot_y = 0

    multiplier = -0.03 * (sensitivity * 3.0) * (fov / 100.0)

    for count in range(len(gunDelta)):

        gunDeltaDeltaValue = gunDelta[count]
  
        currentX = gunDeltaDeltaValue[X]
        currentY = gunDeltaDeltaValue[Y]
        
        dx = (currentX - last_shot_x)
        dy = (currentY - last_shot_y)

        px_x = (dx / multiplier)
        px_y = (dy / multiplier)

        if px_x == -0.0:
            px_x = 0.0
        if px_y == -0.0:
            px_y = 0.0

        NewGunList.append([px_x, px_y])
        
        last_shot_x = currentX
        last_shot_y = currentY
        
        count += 1
    
    return NewGunList

